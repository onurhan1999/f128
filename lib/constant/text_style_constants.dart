import 'dart:ui';

import 'package:flutter/material.dart';
import 'package:google_fonts/google_fonts.dart';

class TextStyleConstants {
  static MonsterratBlack30(BuildContext context) {
    return GoogleFonts.montserrat(
      color: Colors.black,
      fontSize: 30,
    );
  }

  static MonsterratSoftBlack15(BuildContext context) {
    return GoogleFonts.montserrat(
      color: Colors.black45,
      fontSize: 15,
    );
  }

  static MonsterratBlack15(BuildContext context) {
    return GoogleFonts.montserrat(
      color: Colors.black,
      fontSize: 15,
    );
  }

  static MonsterratBoldBlack12(BuildContext context) {
    return GoogleFonts.montserrat(
        color: Colors.black, fontSize: 12, fontWeight: FontWeight.bold);
  }

  static MonsterratBoldBlack15(BuildContext context) {
    return GoogleFonts.montserrat(
        color: Colors.black, fontSize: 15, fontWeight: FontWeight.bold);
  }

  static MonsterratBlack12(BuildContext context) {
    return GoogleFonts.montserrat(
        color: Colors.black, fontSize: 12,);
  }

  static MonsterratBlack10(BuildContext context) {
    return GoogleFonts.montserrat(
      color: Colors.black, fontSize: 10,);
  }

  static MonsterratSoftWhite15(BuildContext context) {
    return GoogleFonts.montserrat(
      color: Colors.white,
      fontWeight: FontWeight.bold,
      fontSize: 15,
    );
  }

  static MonsterratBoldWhite25(BuildContext context) {
    return GoogleFonts.montserrat(
      color: Colors.white,
      fontWeight: FontWeight.bold,
      fontSize: 25,
    );
  }

  static MonsterratBoldOrangeAccent12(BuildContext context) {
    return GoogleFonts.montserrat(
      color: Colors.orangeAccent,
      fontWeight: FontWeight.bold,
      fontSize: 12,
    );
  }
}
